

class Credito:

    def __init__(self, valorCredito, dataCredito, descCredito):
        self.valorCredito = valorCredito
        self.dataCredito  = dataCredito
        self.descCredito  = descCredito





    #  - - - - - - - - - - GETS & SETS - - - - - - - - - -
    ##GETS
    def getValorCredito(self):
        return self.valorCredito

    def getDataCredito(self):
        return self.dataCredito

    def getDescCredito(self):
        return self.descCredito


    ##SETS
    def setValorCredito(self, vc):
        this.valorCredito = vc

    def setDataCredito(self, dc):
        this.dataCredito = dc

    def setDescCredito(self, dsc):
        this.descCredito = dsc


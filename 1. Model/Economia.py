

class Economia:

    def __init__(self, valorNaConta, debito, credito):
        self.valorNaConta = valorNaConta
        self.debito       = debito
        self.credito      = credito





    #  - - - - - - - - - - GETS & SETS - - - - - - - - - -
    ##GETS
    def getValorNaConta(self):
        return self.valorNaConta

    def getDebito(self):
        return self.debito

    def getCredito(self):
        return self.credito


    ##SETS
    def setValorNaConta(self, vnc):
        this.valorNaConta = vnc

    def setDebito(self, d):
        this.debito = d

    def setCredito(self, c):
        this.credito = c
    
